export const generateDistractors = (correctPinyin: string, count: number): string[] => {
  const distractors = new Set<string>();
  const confusingPairs: Record<string, string[]> = {
    'zh': ['z', 'ch', 'j'],
    'ch': ['c', 'sh', 'q'],
    'sh': ['s', 'r', 'x'],
    'z': ['zh', 's'],
    'c': ['ch', 's'],
    's': ['z', 'sh'],
    'n': ['l', 'ng'],
    'l': ['n'],
    'ng': ['n', 'g'],
    'j': ['z', 'q', 'x'],
    'q': ['c', 'j', 'x'],
    'x': ['s', 'j', 'q'],
    'u': ['ü', 'o'],
    'ü': ['u', 'i'],
  };

  for (const [key, values] of Object.entries(confusingPairs)) {
    if (correctPinyin.startsWith(key)) {
      values.forEach(v => {
        const d = correctPinyin.replace(key, v);
        if (d !== correctPinyin) distractors.add(d);
      });
    }
  }

  const allPinyinSamples = VOCABULARY.map(v => v.pinyin);
  let attempts = 0;
  while (distractors.size < count && attempts < 500) {
    const random = allPinyinSamples[Math.floor(Math.random() * allPinyinSamples.length)];
    if (random !== correctPinyin) distractors.add(random);
    attempts++;
  }

  return Array.from(distractors).slice(0, count);
};

export const generateDistractorsWords = (correctWord: Word, count: number): Word[] => {
  const distractors: Word[] = [];
  // Filter distractors to be from the same or lower level (easier)
  const pool = VOCABULARY.filter(w => 
    w.id !== correctWord.id && 
    w.pinyin !== correctWord.pinyin &&
    w.level <= correctWord.level
  );
  
  // If pool is too small (shouldn't happen with YCT levels), fallback
  const finalPool = pool.length >= count ? pool : VOCABULARY.filter(w => w.id !== correctWord.id);
  
  const shuffled = [...finalPool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

